package com.codingdojo.brendan;

public class Rectangle {

	private int height;
	private int length;
	
	public Rectangle(int h, int l) {
		this.height = h;
		this.length = l;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}
}
